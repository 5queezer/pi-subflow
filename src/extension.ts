function formatRecentConversationContext(ctx: ExtensionContext): string | undefined {
	const entries = ctx.sessionManager?.getBranch?.() ?? [];
	const text = entries.map(formatSessionEntryForWorkflowContext).filter(Boolean).join("\n\n").trim();
	if (!text) return undefined;
	return text.length > MAX_WORKFLOW_CONTEXT_CHARS ? `[truncated to recent context]\n${text.slice(-MAX_WORKFLOW_CONTEXT_CHARS)}` : text;
}

function formatSessionEntryForWorkflowContext(entry: SessionEntry): string {
	if (entry.type === "message") return formatAgentMessageForWorkflowContext(entry.message);
	if (entry.type === "custom_message") return [`[${entry.customType}]`, contentToText(entry.content)].filter(Boolean).join("\n");
	if (entry.type === "compaction") return [`[compaction summary]`, entry.summary].filter(Boolean).join("\n");
	if (entry.type === "branch_summary") return [`[branch summary]`, entry.summary].filter(Boolean).join("\n");
	return "";
}

function formatAgentMessageForWorkflowContext(message: any): string {
	if (message.role === "user") return [`User:`, contentToText(message.content)].filter(Boolean).join("\n");
	if (message.role === "assistant") return [`Assistant:`, contentToText(message.content)].filter(Boolean).join("\n");
	if (message.role === "toolResult") return [`Tool result (${message.toolName}):`, contentToText(message.content)].filter(Boolean).join("\n");
	if (message.role === "custom") return [`[${message.customType}]`, contentToText(message.content)].filter(Boolean).join("\n");
	return "";
}

function contentToText(content: unknown): string {
	if (typeof content === "string") return content.trim();
	if (!Array.isArray(content)) return "";
	return content.map((item) => {
		if (typeof item === "string") return item;
		if (!isRecord(item)) return "";
		return typeof item.text === "string" ? item.text : "";
	}).filter(Boolean).join("\n").trim();
}

function isNodeError(error: unknown): error is NodeJS.ErrnoException {
	return error instanceof Error && "code" in error;
}

function isRecord(value: unknown): value is Record<string, unknown> {
	return typeof value === "object" && value !== null;
}

async function confirmPolicies(params: SubflowToolParams, tasks: SubagentTask[], ctx: ExtensionContext): Promise<void> {
	const scope = params.agentScope ?? "user";
	if ((scope === "project" || scope === "both") && params.confirmProjectAgents !== false && ctx.hasUI) {
		const ok = await ctx.ui.confirm("Use project-local agents?", "Project-local agent definitions can override user agents. Continue?");
		if (!ok) throw new Error("project-local agent use declined");
	}
	const needsExternalConfirmation = tasks.some((task) => task.authority === "external_side_effect") && !params.allowExternalSideEffectWithoutConfirmation && ctx.hasUI;
	if (needsExternalConfirmation) {
		const ok = await ctx.ui.confirm("Allow external side effects?", "One or more subagent tasks requested authority: external_side_effect. Continue?");
		if (!ok) throw new Error("external_side_effect authority declined");
	}
}

function inferMode(params: SubflowToolParams): FlowMode {
	if (params.chain) return "chain";
	if (params.dagYaml) return "dag";
	if (params.tasks) return containsNestedWorkflowOrLoop(params.tasks) || params.tasks.some((task) => task.dependsOn?.length || task.role === "verifier") ? "dag" : "parallel";
	if (params.agent && params.task) return "single";
	throw new Error("subflow requires either agent+task, chain, dagYaml, or tasks");
}

function validateNonEmptyStrings(tasks: SubagentTask[]): void {
	for (const task of tasks) {
		if (task.workflow || task.loop) continue;
		if (!task.agent?.trim()) throw new Error("agent must be a non-empty string");
		if (!task.task?.trim()) throw new Error("task must be a non-empty string");
	}
}

function tasksForPolicy(params: SubflowToolParams): SubagentTask[] {
	if (params.tasks) return flattenPolicyTasks(params.tasks);
	if (params.dagYaml) return flattenPolicyTasks(normalizeDagYaml(params).tasks ?? []);
	if (params.chain) return params.chain.map((step) => ({ ...step }));
	if (params.agent && params.task) return [{ ...(params as SubagentTask), agent: params.agent, task: params.task }];
	return [];
}

function flattenPolicyTasks(tasks: SubagentTask[]): SubagentTask[] {
	const flattened: SubagentTask[] = [];
	for (const task of tasks) {
		if (task.workflow?.tasks) flattened.push(...flattenPolicyWorkflowTasks(task.workflow.tasks));
		else if (task.loop?.body) flattened.push(...flattenPolicyLoopTasks(task.loop.body));
		else flattened.push(task);
	}
	return flattened;
}

function flattenPolicyWorkflowTasks(tasks: WorkflowTasksValue): SubagentTask[] {
	if (Array.isArray(tasks)) return tasks.flatMap((task) => task.workflow?.tasks ? flattenPolicyWorkflowTasks(task.workflow.tasks) : task.loop?.body ? flattenPolicyLoopTasks(task.loop.body) : [task]);
	if (isRecord(tasks)) return Object.values(tasks).flatMap((task) => task.workflow?.tasks ? flattenPolicyWorkflowTasks(task.workflow.tasks) : task.loop?.body ? flattenPolicyLoopTasks(task.loop.body) : [task]);
	return [];
}

function flattenPolicyLoopTasks(tasks: NonNullable<SubagentTask["loop"]>["body"]): SubagentTask[] {
	if (Array.isArray(tasks)) return tasks.flatMap((task) => task.workflow?.tasks ? flattenPolicyWorkflowTasks(task.workflow.tasks) : task.loop?.body ? flattenPolicyLoopTasks(task.loop.body) : [task]);
	if (isRecord(tasks)) return Object.values(tasks).flatMap((task) => task.workflow?.tasks ? flattenPolicyWorkflowTasks(task.workflow.tasks) : task.loop?.body ? flattenPolicyLoopTasks(task.loop.body) : [task]);
	return [];
}

function validateToolAllowlist(tasks: SubagentTask[], allowedTools?: string[]): void {
	const allowlist = allowedTools ? new Set(allowedTools) : DEFAULT_ALLOWED_TOOLS;
	for (const task of tasks) {
		for (const tool of task.tools ?? []) {
			if (!allowlist.has(tool)) throw new Error(`unknown or unavailable tool: ${tool}`);
		}
	}
}

function applyAgentDefaults(params: SubflowToolParams, agents: Map<string, AgentDefinition>, defaultCwd: string): SubflowToolParams {
	const apply = <T extends SubagentTask | ChainStep>(task: T): T => {
		const agent = task.agent ? agents.get(task.agent) : undefined;
		const workflow = isWorkflowTask(task) && task.workflow ? {
			...task.workflow,
			tasks: Array.isArray(task.workflow.tasks)
				? task.workflow.tasks.map((child) => apply(child as SubagentTask))
				: isRecord(task.workflow.tasks)
					? Object.fromEntries(Object.entries(task.workflow.tasks).map(([name, child]) => [name, apply(child as SubagentTask)]))
					: task.workflow.tasks,
		} : undefined;
		const loop = "loop" in task && task.loop ? {
			...task.loop,
			body: Array.isArray(task.loop.body)
				? task.loop.body.map((child) => apply(child as SubagentTask))
				: isRecord(task.loop.body)
					? Object.fromEntries(Object.entries(task.loop.body).map(([name, child]) => [name, apply(child as SubagentTask)]))
					: task.loop.body,
		} : undefined;
		return {
			...task,
			workflow,
			loop,
			cwd: task.cwd ?? defaultCwd,
			tools: task.tools ?? agent?.tools,
			model: task.model ?? agent?.model,
			thinking: task.thinking ?? (agent?.thinking as SubagentTask["thinking"] | undefined),
		};
	};
	return {
		...params,
		tasks: params.tasks?.map(apply),
		chain: params.chain?.map(apply),
		...(params.agent && params.task ? apply({ ...(params as SubagentTask), agent: params.agent, task: params.task }) : {}),
	};
}

function containsNestedWorkflowOrLoop(tasks: SubagentTask[]): boolean {
	return tasks.some((task) => Boolean(task.workflow || task.loop));
}

function isWorkflowTask(task: SubagentTask | ChainStep): task is SubagentTask {
	return "workflow" in task;
}

function normalizeLoopBody(tasks: NonNullable<SubagentTask["loop"]>["body"], context: string): NonNullable<SubagentTask["loop"]>["body"] {
	if (Array.isArray(tasks)) return tasks.map((task, index) => normalizeSingleTask({ ...task, name: task.name ?? `${task.agent ?? "task"}-${index + 1}` }));
	if (isRecord(tasks)) return Object.fromEntries(Object.entries(tasks).map(([name, task]) => [name, normalizeSingleTask({ ...task, name: task.name ?? name })]));
	throw new Error(`${context} loop body must be an array or mapping`);
}

function normalizeSingleTask(task: SubagentTask): SubagentTask {
	const normalized = normalizeNestedWorkflows({ tasks: [task] }).tasks?.[0];
	if (!normalized) throw new Error("failed to normalize task");
	return normalized;
}

class ProgressRunner implements SubagentRunner {
	constructor(private readonly inner: SubagentRunner, private readonly progress: ProgressReporter) {}

	async run(input: import("./types.js").RunnerInput, signal?: AbortSignal): Promise<SubagentResult> {
		this.progress.taskStarted(input);
		try {
			const result = await this.inner.run(input, signal);
			this.progress.taskFinished(result);
			return result;
		} catch (error) {
			this.progress.taskFinished({ name: input.name, agent: input.agent ?? "workflow", task: input.task ?? "summary", role: input.role, model: input.model, status: "failed", output: "", error: error instanceof Error ? error.message : String(error), usage: {} });
			throw error;
		}
	}
}

interface ProgressReporter {
	start(): void;
	taskStarted(task: import("./types.js").RunnerInput): void;
	taskFinished(result: SubagentResult): void;
	clear(): void;
}

function createProgressReporter(ctx: ExtensionContext, mode: string, total: number, timeoutSeconds?: number): ProgressReporter | undefined {
	if (!ctx.hasUI) return undefined;
	const ui = ctx.ui as unknown;
	if (typeof ui !== "object" || ui === null || !("setWidget" in ui) || typeof ui.setWidget !== "function") return undefined;
	const setWidget = ui.setWidget;
	const startedAt = Date.now();
	const running = new Map<string, { startedAt: number; task: import("./types.js").RunnerInput }>();
	const results = new Map<string, SubagentResult>();
	let interval: ReturnType<typeof setInterval> | undefined;
	let runningFrame = 0;
	let stopped = false;
	const stop = () => {
		if (interval) clearInterval(interval);
		interval = undefined;
		stopped = true;
	};
	const safeSetWidget = (value: unknown, options?: unknown): boolean => {
		if (stopped) return false;
		try {
			setWidget.call(ui, "pi-subflow-progress", value, options);
			return true;
		} catch {
			stop();
			return false;
		}
	};
	const render = () => {
		if (stopped) return;
		const completed = [...results.values()].filter((result) => result.status === "completed").length;
		const failed = [...results.values()].filter((result) => result.status === "failed").length;
		const skipped = [...results.values()].filter((result) => result.status === "skipped").length;
		const runningCount = [...running.keys()].filter((name) => !results.has(name)).length;
		const status = failed > 0 ? "failed" : completed + skipped >= total && total > 0 ? "completed" : "running";
		const timeout = timeoutSeconds ? ` · ${timeoutSeconds}s timeout` : "";
		const elapsed = formatDuration(Date.now() - startedAt);
		const runningIcon = statusIcon("running", runningFrame);
		const taskLines = [
			...[...results.values()].map((result) => `${statusIcon(result.status, runningFrame)} ${result.name ?? result.agent} ${formatTaskIdentity(result)}${result.error ? `: ${result.error}` : result.output ? ` → ${firstLine(result.output)}` : ""}`),
			...[...running.entries()].filter(([name]) => !results.has(name)).map(([name, state]) => `${runningIcon} ${name} ${formatTaskIdentity(state.task)} · ${formatDuration(Date.now() - state.startedAt)} elapsed`),
		];
		const lines = [
			`subflow · ${mode} · ${status}`,
			`${total} task${total === 1 ? "" : "s"} · ${runningCount} running · ${completed} completed · ${failed} failed · ${skipped} skipped · ${elapsed} elapsed${timeout}`,
			...(taskLines.length ? taskLines : ["waiting to start"]),
		];
		if (safeSetWidget(lines, { placement: "belowEditor" })) runningFrame += 1;
	};
	return {
		start() {
			render();
			if (stopped) return;
			interval = setInterval(render, PROGRESS_FRAME_INTERVAL_MS);
			interval.unref?.();
		},
		taskStarted(task) {
			running.set(task.name, { startedAt: Date.now(), task });
			render();
		},
		taskFinished(result) {
			results.set(result.name ?? result.agent, result);
			running.delete(result.name ?? result.agent);
			render();
		},
		clear() {
			const shouldClearWidget = !stopped;
			stop();
			if (!shouldClearWidget) return;
			try {
				setWidget.call(ui, "pi-subflow-progress", undefined);
			} catch {
				// Ignore stale UI contexts during session replacement/reload.
			}
		},
	};
}

async function runSelectedFlow(mode: FlowMode, params: SubflowToolParams, options: ExecutionOptions): Promise<FlowResult> {
	if (mode === "single") return runSingle({ agent: params.agent ?? "", task: params.task ?? "", cwd: (params as SubagentTask).cwd, role: (params as SubagentTask).role, tools: (params as SubagentTask).tools, model: (params as SubagentTask).model, thinking: (params as SubagentTask).thinking }, options);
	if (mode === "chain") return runChain({ chain: params.chain ?? [] }, options);
	if (mode === "dag") return runDag({ tasks: params.tasks ?? [] }, options);
	return runParallel({ tasks: params.tasks ?? [] }, options);
}

